/**
 * Compute level from total XP.
 *
 * @param {number} xp
 * @returns {number}
 */
function levelForXp(xp) {
  let level = 1
  for (const bracket of LEVEL_BRACKETS) {
    if (xp >= bracket.minXp) level = bracket.level
    else break
  }
  return level
}

